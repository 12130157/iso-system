package vn.edu.hungvuongaptech.model;

public class ChiTietDiemDanhModel {
	private String maChiTietDiemDanh;
	private String maTVDD;
	private String ngayHoc;
	private String tinhTrangDiHoc;
	private String thang;
	private String ngay;
	private String nam;
	
	public String getThang() {
		return thang;
	}
	public void setThang(String thang) {
		this.thang = thang;
	}
	public String getNgay() {
		return ngay;
	}
	public void setNgay(String ngay) {
		this.ngay = ngay;
	}
	public String getNam() {
		return nam;
	}
	public void setNam(String nam) {
		this.nam = nam;
	}
	public String getMaChiTietDiemDanh() {
		return maChiTietDiemDanh;
	}
	public void setMaChiTietDiemDanh(String maChiTietDiemDanh) {
		this.maChiTietDiemDanh = maChiTietDiemDanh;
	}
	public String getMaTVDD() {
		return maTVDD;
	}
	public void setMaTVDD(String maTVDD) {
		this.maTVDD = maTVDD;
	}
	public String getNgayHoc() {
		return ngayHoc;
	}
	public void setNgayHoc(String ngayHoc) {
		this.ngayHoc = ngayHoc;
	}
	public String getTinhTrangDiHoc() {
		return tinhTrangDiHoc;
	}
	public void setTinhTrangDiHoc(String tinhTrangDiHoc) {
		this.tinhTrangDiHoc = tinhTrangDiHoc;
	}
	
}
